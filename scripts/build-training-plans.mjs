#!/usr/bin/env node
/**
 * Parse seeds/training-plans.csv into a typed, bundled catalog module
 * (src/lib/training-plans/catalog.ts). Run after editing the CSV:
 *   node scripts/build-training-plans.mjs
 *
 * The generated module is pure data (no fs/runtime parsing) so it bundles
 * cleanly into the web app and the later Tauri build.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join } from "node:path";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");
const CSV = join(root, "seeds/training-plans.csv");
const OUT = join(root, "src/lib/training-plans/catalog.ts");

const MI = 1609.34;
const KM = 1000;

// Map CSV sport labels to our five modalities (null = rest / no workout).
function toModality(sport, type) {
  const s = (sport || "").toLowerCase();
  if (s === "rest") return null;
  if (s === "run" || s === "walk") return "run";
  if (s === "bike" || s === "bike+run") return "bike";
  if (s === "swim") return "swim";
  // Cross-training / multisport days have no single discipline; bucket them
  // under "core" (our general-fitness modality).
  if (s === "cross" || s === "multi") return "core";
  // Fall back on the workout type for anything unexpected.
  const t = (type || "").toLowerCase();
  if (t.includes("run")) return "run";
  if (t.includes("bike")) return "bike";
  if (t.includes("swim")) return "swim";
  return "core";
}

// Parse "1.5 mi" / "30 min" / "20-30 min" / "10K (6.2 mi)" into meters/seconds.
function parseAmount(raw) {
  const out = { durationS: null, distanceM: null };
  if (!raw) return out;
  const mi = raw.match(/([\d.]+)\s*mi\b/i);
  const km = raw.match(/([\d.]+)\s*km\b/i);
  const k = raw.match(/([\d.]+)\s*K\b/);
  const min = raw.match(/([\d.]+)(?:\s*-\s*[\d.]+)?\s*min\b/i);
  const hr = raw.match(/([\d.]+)\s*hr\b/i);
  if (mi) out.distanceM = Math.round(Number(mi[1]) * MI);
  else if (km) out.distanceM = Math.round(Number(km[1]) * KM);
  else if (k) out.distanceM = Math.round(Number(k[1]) * KM);
  if (hr) out.durationS = Math.round(Number(hr[1]) * 3600);
  else if (min) out.durationS = Math.round(Number(min[1]) * 60);
  return out;
}

// Minimal RFC-4180 CSV parser (handles quoted fields with commas/quotes).
function parseCsv(text) {
  const rows = [];
  let row = [];
  let field = "";
  let inQuotes = false;
  for (let i = 0; i < text.length; i++) {
    const c = text[i];
    if (inQuotes) {
      if (c === '"') {
        if (text[i + 1] === '"') {
          field += '"';
          i++;
        } else inQuotes = false;
      } else field += c;
    } else if (c === '"') inQuotes = true;
    else if (c === ",") {
      row.push(field);
      field = "";
    } else if (c === "\n" || c === "\r") {
      if (c === "\r" && text[i + 1] === "\n") i++;
      row.push(field);
      field = "";
      if (row.length > 1 || row[0] !== "") rows.push(row);
      row = [];
    } else field += c;
  }
  if (field !== "" || row.length) {
    row.push(field);
    rows.push(row);
  }
  return rows;
}

const rows = parseCsv(readFileSync(CSV, "utf8"));
const header = rows[0];
const idx = (name) => header.indexOf(name);
const col = {
  plan_id: idx("plan_id"),
  plan_name: idx("plan_name"),
  source: idx("source"),
  distance: idx("distance"),
  level: idx("level"),
  total_weeks: idx("total_weeks"),
  week: idx("week"),
  day_of_week: idx("day_of_week"),
  day_num: idx("day_num"),
  workout_type: idx("workout_type"),
  sport: idx("sport"),
  duration_or_distance: idx("duration_or_distance"),
  intensity: idx("intensity"),
  workout_description: idx("workout_description"),
  notes: idx("notes"),
};

const plans = new Map();
for (let r = 1; r < rows.length; r++) {
  const c = rows[r];
  const id = c[col.plan_id];
  if (!id) continue;
  if (!plans.has(id)) {
    plans.set(id, {
      id,
      name: c[col.plan_name],
      source: c[col.source],
      distance: c[col.distance],
      level: c[col.level],
      weeks: Number(c[col.total_weeks]),
      workouts: [],
    });
  }
  const amt = parseAmount(c[col.duration_or_distance]);
  plans.get(id).workouts.push({
    week: Number(c[col.week]),
    dayOfWeek: c[col.day_of_week],
    dayNum: Number(c[col.day_num]),
    type: c[col.workout_type],
    modality: toModality(c[col.sport], c[col.workout_type]),
    durationS: amt.durationS,
    distanceM: amt.distanceM,
    intensity: c[col.intensity] || "",
    description: c[col.workout_description] || "",
    notes: c[col.notes] || "",
  });
}

const list = [...plans.values()];
for (const p of list) p.workouts.sort((a, b) => a.dayNum - b.dayNum);

const banner =
  "// AUTO-GENERATED by scripts/build-training-plans.mjs from\n" +
  "// seeds/training-plans.csv. Do not edit by hand; edit the CSV and rerun.\n";
const body =
  banner +
  'import type { TrainingPlan } from "./types.js";\n\n' +
  `export const PLANS: TrainingPlan[] = ${JSON.stringify(list, null, 2)};\n`;

writeFileSync(OUT, body);
console.log(
  `wrote ${OUT}: ${list.length} plans, ${list.reduce((n, p) => n + p.workouts.length, 0)} workouts`,
);
